source("R/genPattern_RH.R")

set.seed(123)

N <- 1000
J <- 30

# Latent trait
theta <- rnorm(N)

# Generate item parameters
pars <- cbind(
  a = rlnorm(J, meanlog = 0, sdlog = 0.2),
  b = rnorm(J, mean = 0, sd = 1),
  d = rnorm(J, mean = 0, sd = 0.5)
)

dat <- genPattern_RH(
  th = theta,
  pars = pars,
  seed = 346536
)


# fit model with mirt

library(mirt)


# RH------------------------------------------------------------------------------------------------------
name <- 'RH'
par <- c(a = 1.0, b = 0.0, delta = 0.0)
est <- c(TRUE, TRUE, TRUE)

P.RH <- function(par, Theta, ncat) {
  a <- par[1]
  b <- par[2]
  delta <- par[3]

  denom <- (1 + exp(-delta * Theta))^(-1 / 2)

  P1 <- pnorm((a * Theta + b) / denom)
  P1 <- as.vector(P1)
  cbind(1 - P1, P1)
}

x_rh <- createItem(name, par = par, est = est, P = P.RH)

mod_rh <- mirt(dat, 1, itemtype = 'RH', customItems = list(RH = x_rh))

# good recovery
pars - coef(mod_rh, simplify = TRUE)$items

