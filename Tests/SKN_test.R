source("R/genPattern_SKN.R")

set.seed(123)

N <- 1000
J <- 30

# Latent trait
theta <- rnorm(N)

# Generate item parameters
pars <- cbind(
  a = rlnorm(J, meanlog = 0, sdlog = 0.2),
  b = rnorm(J, mean = 0, sd = 1),
  lambda = rnorm(J, mean = 0, sd = 0.5)
)

dat <- genPattern_SKN(
  th = theta,
  pars = pars,
  seed = 346536
)


# fit model with mirt

library(mirt)


# SKN----------------------------------------------------------------------------------------------------
library(sn)
name <- 'SKN'
par <- c(a = 1.0, b = 0.0, lambda = 0.0)
est <- c(TRUE, TRUE, TRUE)

P.SKN <- function(par, Theta, ncat) {
  a <- par[1]
  b <- par[2]
  lambda <- par[3]

  P1 <- psn(a * (Theta - b), xi = 0, omega = 1, alpha = lambda)
  P1 <- as.vector(P1)
  cbind(1 - P1, P1)
}

x_skn <- createItem(name, par = par, est = est, P = P.SKN)

# pretty slow, may have to do with the psn() function in the definition
mod_skn <- mirt(dat, 1, itemtype = 'SKN', customItems = list(SKN = x_skn))

# not good
pars - coef(mod_skn, simplify = TRUE)$items

